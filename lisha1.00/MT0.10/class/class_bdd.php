<?php
/**==================================================================
 * Database connexion
 ====================================================================*/		
	class class_bdd
	{
		//==================================================================
		// Public class attributs
		//==================================================================
		public $link_mt;
		//==================================================================
		
		//==================================================================
		// Private class attributs
		//==================================================================
		private $c_ssid;
		//==================================================================
		
		
		/**==================================================================
		 * Constructor of class_bdd
		 * @param p_ssid : SSID identifier
		====================================================================*/		
		public function __construct($p_ssid = 0)
		{
			$this->c_ssid = $p_ssid;
		}
		/*===================================================================*/	
		
	
		/**==================================================================
		 * Create an active connexion to database
		 * @protected
		====================================================================*/		
		protected function db_connexion()
		{
			//==================================================================
			// Main database connexion schema
			//==================================================================
			$this->link_mt = new mysqli(__MAGICTREE_DATABASE_HOST__, __MAGICTREE_DATABASE_USER__, __MAGICTREE_DATABASE_PASSWORD__, __MAGICTREE_DATABASE_SCHEMA__);
			if ($this->link_mt->connect_errno)
			{
			    error_log("Database connexion error : ".$this->link_mt->connect_error);
			    die();
			}
			$this->link_mt->set_charset("utf8"); // Usefull for php 5.4
			//==================================================================
		}
		/*===================================================================*/	
	}
?>